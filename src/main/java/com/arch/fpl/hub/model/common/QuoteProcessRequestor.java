package com.arch.fpl.hub.model.common;

public class QuoteProcessRequestor {

	private String quoteRequestorCode;
	private String quoteRequestorName;
	private String quoteRequestorDescription;
	
	public String getQuoteRequestorCode() {
		return quoteRequestorCode;
	}
	public void setQuoteRequestorCode(String quoteRequestorCode) {
		this.quoteRequestorCode = quoteRequestorCode;
	}
	public String getQuoteRequestorName() {
		return quoteRequestorName;
	}
	public void setQuoteRequestorName(String quoteRequestorName) {
		this.quoteRequestorName = quoteRequestorName;
	}
	public String getQuoteRequestorDescription() {
		return quoteRequestorDescription;
	}
	public void setQuoteRequestorDescription(String quoteRequestorDescription) {
		this.quoteRequestorDescription = quoteRequestorDescription;
	}
	
	
}
